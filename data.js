let container = document.getElementById("container");
let lsData=[]
let displayData = (data) => {
  // console.log("working");
  container.innerHTML = "";
  data.forEach((ele) => {
    let x = Math.random() * 2 + 3;
    x = x.toFixed(1);
    // console.log(x);
    let Pname = document.createElement("p");
    Pname.innerText = ele.title;
    let Pimage = document.createElement("img");
    Pimage.src = ele.image;
    let Prating = document.createElement("p");
    Prating.innerText = "Rating : " + x;
    Prating.style.color = "red";
    let Pprice = document.createElement("p");
    Pprice.innerText = "₹" + ele.price;

    let div = document.createElement("div");
    div.addEventListener("click", () => {
      lsData.push(ele)
      localStorage.setItem("furniture", JSON.stringify(lsData))
      window.location.href = "./singleProduct.html";
    });
    div.append(Pimage, Pname, Prating, Pprice);
    container.append(div);
  });
};
displayData(data);

function filter_page(ele) {
  // console.log(ele);
  // let filter_data = ele.price;
  let filter = document.getElementById("filter-product").value;
  if (filter === "LH") {
    ele.sort(function (a, b) {
      return +a.price - +b.price; //accending order
    });
    displayData(ele);
  } else if (filter === "HL") {
    ele.sort(function (a, b) {
      return +b.price - +a.price; //accending order
    });
    displayData(ele);
  }
}

